#include<iostream>
#include<string>
using namespace std;
int main()
{
	//creating variables
	int myNum = 10;
	float myfloatNum = 5.5;
	double mydoubleNum = 55.55;
	char myLetter = 'D';
	bool myBoolean = true;
	string myString = "Hello";
	
	//print variable values
	cout << " int = " << myNum << endl;
	cout << " float = " << myfloatNum << endl;
	cout << " double = " << mydoubleNum << endl;
	cout << " char = " << myLetter << endl;
	cout << " bool = " << myBoolean << endl;
	cout << " string = " << myString << endl;
	
	return 0;
}
